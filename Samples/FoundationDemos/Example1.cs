namespace Samples;

/// <summary>
/// This class contains examples of declaring and initializing different types of variables.
/// </summary>
public class Example1
{
    public static void Run()
    {
            // Integer variable
            int myInt = 10;
            Console.WriteLine("My integer value is: " + myInt);

            // Double variable
            double myDouble = 3.14;
            Console.WriteLine("My double value is: " + myDouble);

            // Float variable
            float myFloat = 1.23f;
            Console.WriteLine("My float value is: " + myFloat);

            // Decimal variable
            decimal myDecimal = 123.45m;
            Console.WriteLine("My decimal value is: " + myDecimal);

            // Boolean variable
            bool myBool = true;
            Console.WriteLine("My boolean value is: " + myBool);

            // String variable
            string myString = "Hello, world!";
            Console.WriteLine("My string value is: " + myString);

            // Char variable
            char myChar = 'A';
            Console.WriteLine("My char value is: " + myChar);

            // Byte variable
            byte myByte = 255;
            Console.WriteLine("My byte value is: " + myByte);

            // Object variable
            object myObject = 123;
            Console.WriteLine("My object value is: " + myObject);
    }
}
