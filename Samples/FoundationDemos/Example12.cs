namespace Samples;

/// <summary>
/// This class contains an example of using the var keyword in C#.
/// </summary>
public class Example12
{
    public static void Run()
    {
            // Var keyword example
            var message = "Hello, world!";
            var number = 42;
            var pi = 3.14159;

            Console.WriteLine(message);
            Console.WriteLine(number);
            Console.WriteLine(pi);
    }
}